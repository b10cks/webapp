<script setup lang="ts">
import { Textarea } from '~/components/ui/textarea'
import { FormField } from '~/components/ui/form'

defineProps<{
  item: MarkdownSchema & { key: string }
  originalValue: string
  modelValue: string
  isMachineTranslated?: boolean
}>()

const emit = defineEmits<{
  'update:modelValue': [value: string]
}>()

const updateValue = (e: Event) => {
  const target = e.target as HTMLTextAreaElement
  emit('update:modelValue', target.value)
}
</script>

<template>
  <div
    class="grid grid-cols-2 gap-4 py-2"
    :aria-labelledby="`${item.key}-label`"
  >
    <FormField
      :name="`${item.key}-original`"
      :label="item.name || item.key"
      hide-label
    >
      <Textarea
        :model-value="originalValue"
        disabled
        rows="4"
        class="resize-none"
        :aria-label="`Original ${item.name || item.key}`"
      />
    </FormField>
    <FormField
      :label="item.name || item.key"
      :name="`${item.key}-translation`"
      hide-label
    >
      <Textarea
        :model-value="modelValue"
        rows="4"
        :class="['resize-none', isMachineTranslated && 'ring-1 ring-ai']"
        :placeholder="originalValue"
        @input="updateValue"
      />
    </FormField>
  </div>
</template>